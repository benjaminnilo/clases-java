package ejercicios;

public class Ejercicio13 {

	public static void main(String[] args) {
		int x = 0;

		while (x < 100) {
			x = x + 1;
			System.out.println("La cuenta es: " + x);
		}
		
		for (int increment = 0; increment <= 100; increment++) {
			System.out.println("El valor es:" + increment);
		}
		
	}

}
